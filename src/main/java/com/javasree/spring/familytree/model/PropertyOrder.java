package com.javasree.spring.familytree.model;

public class PropertyOrder {

	private Long propertyOrderId;
	private Long propertyOder;
	
	public PropertyOrder(){
		super();
	}

	public Long getPropertyOrderId() {
		return propertyOrderId;
	}

	public void setPropertyOrderId(Long propertyOrderId) {
		this.propertyOrderId = propertyOrderId;
	}

	public Long getPropertyOder() {
		return propertyOder;
	}

	public void setPropertyOder(Long propertyOder) {
		this.propertyOder = propertyOder;
	}
}
